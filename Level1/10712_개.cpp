/*
 |\_/|
|q p|   /}
( 0 )"""\
|"^"`    |
||_/=\\__|

개 출력!

"" 안에서 \", \\ 로 써야 올바른 출력 가능.
마지막 줄 \를 2번 출력하고 싶으면 \를 4번 쓴다.

*/
#include<iostream>
using namespace std;
int main() {
    cout << "|\\_/|" << endl;
    cout << "|q p|   /}" << endl;
    cout << "( 0 )\"\"\"\\" << endl;
    cout << "\|\"^\"`    |" << endl;
    cout << "||_/=\\\\__|" << endl;

}